//we use curly braces when there are multiple comments to be run 
const sum = (a,b)=>{
    let x = a+b;
    console.log(x);
}
sum(5,3);

//we dont need to use curly braces when there is only one command as shown below
const diff = (a,b) => console.log(a-b);
diff(5,3);


